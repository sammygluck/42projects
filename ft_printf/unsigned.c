#include "libprintf.h"

void	u_nbr(unsigned int nbr)
{
	char	n;

	if (nbr >= 10)
	{
		u_nbr(nbr / 10);
		nbr = nbr % 10;
	}
	n = nbr + '0';
	write(1, &n, 1);
}
